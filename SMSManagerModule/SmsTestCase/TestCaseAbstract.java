
public abstract class TestCaseAbstract {
	protected static void log(String msg){
		System.out.println(msg);
	}
	
	protected static void log(Object msg){
		System.out.println(msg);
	}
	
	protected static void log(int msg){
		System.out.println(msg);
	}
	
	protected static void log(long msg){
		System.out.println(msg);
	}
}
